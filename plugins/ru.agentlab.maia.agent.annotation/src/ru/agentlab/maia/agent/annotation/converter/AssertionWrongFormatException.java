package ru.agentlab.maia.agent.annotation.converter;

public class AssertionWrongFormatException extends AssertionFormatException {

	private static final long serialVersionUID = 1L;

	public AssertionWrongFormatException() {
		super();
	}

	public AssertionWrongFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public AssertionWrongFormatException(String message) {
		super(message);
	}

	public AssertionWrongFormatException(Throwable cause) {
		super(cause);
	}

}
