package ru.agentlab.maia.agent.annotation.converter;

public class LiteralWrongFormatException extends LiteralFormatException {

	private static final long serialVersionUID = 1L;

	public LiteralWrongFormatException() {
		super();
	}

	public LiteralWrongFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public LiteralWrongFormatException(String message) {
		super(message);
	}

	public LiteralWrongFormatException(Throwable cause) {
		super(cause);
	}

}
